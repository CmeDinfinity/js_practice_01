let a = 10;
let b = 10;

let operator = "+"

if (operator === "+"){
    console.log(a + b);
    
}

else if (operator === "-"){
    console.log(a - b);
    
}

else if (operator === "*"){
    console.log(a * b);
    
}

else if (operator === "/"){
    console.log(a / b);
    
}

else {
    console.log("Invalid operator");
    
}

